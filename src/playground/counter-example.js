// State
let count = 0;

// Event Handlers
const addOne = () => {
  count++;
  renderCounterApp();
};

const minusOne = () => {
  count--;
  renderCounterApp();
};

const reset = () => {
  count = 0;
  renderCounterApp();
};

// Rendering Template
const renderCounterApp = () => {
  // Template
  const templateTwo = (
    <div>
      <h1>Count: {count}</h1>
      <button onClick={addOne}>+1</button>
      <button onClick={minusOne}>-1</button>
      <button onClick={reset}>Reset</button>
    </div>
  );
  // Rendering
  ReactDOM.render(templateTwo, appRoot);
};

renderCounterApp();
